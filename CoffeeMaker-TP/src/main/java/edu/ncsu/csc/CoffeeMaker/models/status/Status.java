package edu.ncsu.csc.CoffeeMaker.models.status;

/**
 * The enumeration class for different stages an order can go through
 * 
 * @author Meles Meles
 *
 */
public enum Status {
    /**
     * Represents that an order has been placed.
     */
    Order_Placed,

    /**
     * Represents that an order has been fulfilled.
     */
    Order_Fulfilled,

    /**
     * Represents that an order has been completed.
     */
    Order_Completed
}
